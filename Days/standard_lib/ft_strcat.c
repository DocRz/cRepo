char	*ft_strcat(char *dest, char *src)
{
	int c;
	int i;

	c = 0;
	while (dest[c])
		c++;
	i = 0;
	while (src[i])
		dest[c++] = src[i++];
	dest[c] = '\0';
	return (dest);
}
